/// Fabric credentials generated by [MatterFabricPort.exportFabricForController].
/// Sent to the controller via [FluxCoapService.provisionFabric].
class FabricExportData {
  final Uint8List rootCaTlv;
  final Uint8List nocTlv;
  final Uint8List opPrivKey;
  final Uint8List ipk;
  final int       fabricId;

  const FabricExportData({
    required this.rootCaTlv,
    required this.nocTlv,
    required this.opPrivKey,
    required this.ipk,
    required this.fabricId,
  });
}

