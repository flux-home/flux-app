/// Returns the column-width of a character in the dot matrix (3 for
/// half-width glyphs, 5 for everything else).
int dotMatrixCharCols(String ch) => switch (ch) {
  '.' || ' ' || ':' || ',' => 3,
  _ => 5,
};

/// Strips all non-ASCII-printable characters from [text] (including emoji
/// and Unicode) so it can be safely fed to [paintDotMatrix].
/// Characters missing from the glyph table fall back to '-' inside the painter.
String dotMatrixNormalize(String text) => text.codeUnits
    .where((c) => c >= 0x20 && c <= 0x7E)
    .map(String.fromCharCode)
    .join()
    .trim()
    .toUpperCase();

// ─────────────────────────────────────────────────────────────────────────────
// paintDotMatrix — free function, reused by the widget and other painters
// ─────────────────────────────────────────────────────────────────────────────

/// Renders [text] as a 5×7 dot-matrix directly onto [canvas], centred at
/// [centre], scaled to fit within [maxWidth] × [maxHeight].
void paintDotMatrix(
  Canvas canvas,
  Offset centre,
  String text, {
  required double maxWidth,
  required double maxHeight,
  required Color color,
}) {
  final chars = text.characters.toList();
  if (chars.isEmpty) return;
  final totalCols =
      chars.fold(0, (s, c) => s + dotMatrixCharCols(c)) + (chars.length - 1);
  const gap  = 2.0;
  final step = math.min((maxWidth + gap) / totalCols, (maxHeight + gap) / 7);
  final r    = (step - gap) / 2;
  final matW = step * totalCols - gap;
  final matH = step * 7 - gap;
  final ox   = centre.dx - matW / 2;
  final oy   = centre.dy - matH / 2;
  final p    = Paint()..color = color..style = PaintingStyle.fill;
  double cx  = ox;
  for (final ch in chars) {
    final g    = dotMatrixGlyphs[ch] ?? dotMatrixGlyphs['-']!;
    final cols = dotMatrixCharCols(ch);
    for (int row = 0; row < 7; row++) {
      for (int col = 0; col < cols; col++) {
        if (((g[row] >> ((cols - 1) - col)) & 1) == 1) {
          canvas.drawCircle(
            Offset(cx + col * step + step / 2, oy + row * step + step / 2),
            r, p,
          );
        }
      }
    }
    cx += cols * step + step;
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// DotMatrixPainter — auto-scales to fill the available widget size
// ─────────────────────────────────────────────────────────────────────────────

/// Renders [text] as a 5 × 7 dot-matrix in the given canvas, centred and
/// scaled to fill the available size.
///
/// [litColor]  – colour of lit (foreground) dots.
/// [dimColor]  – optional colour of unlit (background) dots; omitted if null.
class DotMatrixPainter extends CustomPainter {
  final String text;
  final Color  litColor;
  final Color? dimColor;

  const DotMatrixPainter({
    required this.text,
    required this.litColor,
    this.dimColor,
  });

  @override
  void paint(Canvas canvas, Size size) {
    final chars = text.characters.toList();
    final n     = chars.length;
    if (n == 0) return;

    final totalCols =
        chars.fold(0, (s, c) => s + dotMatrixCharCols(c)) + (n - 1);

    const gap  = 2.0;
    final stepW = (size.width  + gap) / totalCols;
    final stepH = (size.height + gap) / 7;
    final step  = math.min(stepW, stepH);
    final r     = (step - gap) / 2;

    final matW = step * totalCols - gap;
    final matH = step * 7         - gap;
    final ox   = (size.width  - matW) / 2;
    final oy   = (size.height - matH) / 2;

    final litPaint = Paint()..color = litColor ..style = PaintingStyle.fill;
    final dimPaint = dimColor != null
        ? (Paint()..color = dimColor!..style = PaintingStyle.fill)
        : null;

    double cx = ox;
    for (final ch in chars) {
      final glyph = dotMatrixGlyphs[ch] ?? dotMatrixGlyphs['-']!;
      final cols  = dotMatrixCharCols(ch);
      for (int row = 0; row < 7; row++) {
        final bits = glyph[row];
        for (int col = 0; col < cols; col++) {
          final lit = ((bits >> ((cols - 1) - col)) & 1) == 1;
          if (lit) {
            canvas.drawCircle(
              Offset(cx + col * step + step / 2, oy + row * step + step / 2),
              r, litPaint,
            );
          } else if (dimPaint != null) {
            canvas.drawCircle(
              Offset(cx + col * step + step / 2, oy + row * step + step / 2),
              r * 0.55, dimPaint,
            );
          }
        }
      }
      cx += cols * step + step;
    }
  }

  @override
  bool shouldRepaint(DotMatrixPainter old) =>
      old.text != text || old.litColor != litColor || old.dimColor != dimColor;
}
